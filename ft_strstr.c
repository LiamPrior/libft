#include "libft.h"

char	*ft_strstr(const char *big, const char *little)
{
	int i;
	int x;

	i = 0;
	if (!*little)
		return ((char *)big);
	while (big[i])
	{
		x = 0;
		while ((big[i] == little[x] && big[i]))
		{
			i++;
			x++;
		}
		if (!little[x])
			return ((char *)&big[i - x]);
		i = (i - x) + 1;
	}
	return (NULL);
}
